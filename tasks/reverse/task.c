unsigned char encFlag[] = {0x9b, 0x17, 0xb2, 0x78, 0xb5, 0x8d, 0x36, 0x94, 0x2c, 0x4d, 0x0c, 0x2f, 0x9c, 0xdb, 0x5b, 0x8a, 0x54, 0x04, 0x6e, 0x85, 0xe9, 0x7c, 0x4b, 0xb2, 0x53, 0x8a, 0x00};
