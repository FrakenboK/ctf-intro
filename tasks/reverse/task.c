char compileTime[] = __TIME__; 

void printFlag() {
    unsigned char decrypted[100];

    for (size_t i = 0; i < sizeof(encFlag); i++) {
        decrypted[i] = key[i] ^ encFlag[i];
    }

    decrypted[sizeof(encFlag)] = '\0';

    printf("Here is your flag: %s", decrypted);
}

bool compareTime(char * time) {
    return strcmp(time, compileTime) == 0;
}

int main(int argc, char *argv[]) {
    if (argc < 2) {
        printf("Wrong time bro:(");
        return 0;
    }

    if (compareTime(argv[1])) {
        printFlag();
        return 0;
    }
    printf("Wrong time bro:(");
}

